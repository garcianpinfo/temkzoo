        <p>
            O Cone de algas recheado com peixe cru e arroz, o tradicional temaki,
            nasceu no século 19 como uma opção de comida rápida para os japoneses que
            moravam em Edo, cidade que posteriormente virou a capital nipônica: Tóquio. 
            Na época, os quiosques de sushi eram a opção mais popular, justamente pela 
            facilidade no preparo da comida: o sushiman enrolava na própria mão uma espécie
            de charutinho com os recheios disponíveis e passava ao cliente. O molho shoyu era 
            oferecido em bacias comunitárias dispostas sobre o balcão. Para evitar que ele pingasse 
            nas roupas das pessoas, em 1924 os japoneses inventaram o formato de cone.
            Não é novidade para ninguém que os temakis caíram no gosto dos brasileiros,
            mas você sabia que temakerias foram inventadas aqui? Segundo o crítico de gastronomia 
            e professor de gastronomia da Mackenzie, Arnaldo Lorençato, no Japão não é hábito comer
            o prato fora de casa. “Os restaurantes especializados em temaki são uma invenção brasileira”,
            explicou o professor e crítico.
            Peixes, algas, cogumelos, tofu, arroz e outros ingredientes da culinária japonesa carregam o 
            segredo milenar da longevidade do povo japonês. Uma pesquisa publicada recentemente comprovou 
            que as pessoas que seguem uma dieta japonesa saudável têm cerca de 40% menos sintomas de depressão 
            do que aquelas que não a seguem. Dá para entender o porquê destes números com um olhar mais atento sobre um cardápio 
            básico da comida japonesa. As preparações levam pouca ou nenhuma gordura saturada (o tipo que é nocivo ao organismo)
            tornando os pratos mais leves, os alimentos crus preservam 100% dos nutrientes e, além disso, são ricos em substâncias 
            importantes para preservar a saúde, como o ômega 3 do salmão, que previne contra doenças cardiovasculares, ou o lentinan
            dos cogumelos, que reforça o sistema imunológico.
            Até mesmo quem está de regime pode colher as vantagens e prazeres da culinária sem peso na consciência.
